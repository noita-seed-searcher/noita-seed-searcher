var allSpellsUnlocked = true

func isSpellUnlocked(i int) bool {
	if allSpellsUnlocked {
		return true
	}
	return !unlockableSpells[allSpells[i].name]
}

func GetRandomActionWithType(x, y float64, level, spellType int, worldSeed uint32, offset int) string {
	if level > 10 {
		level = 10
	}
	p := newPrng(0)
	p.setRandomSeed(worldSeed+uint32(offset), x, y)
	var sum float64
	for i, spell := range allSpells {
		if !isSpellUnlocked(i) {
			continue
		}
		if spell.spellType == spellType {
			sum += spell.spawnProbabilities[level]
		}
	}
	accumulated := sum * p.next()
	for i, spell := range allSpells {
		if !isSpellUnlocked(i) {
			continue
		}
		if spell.spellType != spellType {
			continue
		}
		prob := spell.spawnProbabilities[level]
		if prob > 0.0 && prob >= accumulated {
			return spell.name
		}
		accumulated -= prob
	}
	return ""
}

func GetRandomAction(x, y float64, level int, worldSeed uint32, offset int) string {
	if level > 10 {
		level = 10
	}
	p := newPrng(0)
	p.setRandomSeed(worldSeed+uint32(offset), x, y)
	var sum float64
	for i, spell := range allSpells {
		if !isSpellUnlocked(i) {
			continue
		}
		sum += spell.spawnProbabilities[level]
	}
	accumulated := sum * p.next()
	for i, spell := range allSpells {
		if !isSpellUnlocked(i) {
			continue
		}
		prob := spell.spawnProbabilities[level]
		if prob == 0.0 {
			continue
		}
		if prob >= accumulated {
			return spell.name
		}
		accumulated -= prob
	}
	return ""
}

func MakeRandomSpell(p *NollaPrng) string {
	for {
		itemno := p.random(1, len(allSpells))
		i := itemno - 1
		if !isSpellUnlocked(i) {
			continue
		}
		spell := allSpells[i]
		var totalProb float64
		for _, v := range spell.spawnProbabilities {
			totalProb += v
		}
		canSpawn := spell.name == "SEA_SWAMP" || spell.name == "SUMMON_PORTAL" || totalProb > 0
		if canSpawn {
			return spell.name
		}
	}
}
